#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c[12];
	int		i;

	i = 0;
	if (n == 0)
		write(fd, "0", 1);
	else
	{
		if (n < 0)
		{
			c[i] = n % (-10) * (-1) + '0';
			n = n / 10 * (-1);
			write(fd, "-", 1);
			i++;
		}
		while (n > 0)
		{
			c[i] = n % 10 + '0';
			i++;
			n = n / 10;
		}
		while (i-- > 0)
			ft_putchar_fd(c[i], fd);
	}
}
